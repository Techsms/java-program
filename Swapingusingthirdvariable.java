public class Swapingusingthirdvariable{
    public static void main(String[] args) {
        int x=10;
        int y=20;
        int z;
        System.out.println("Before swaping numbers:"+x +y);
        z=y;
        y=x;
        x=z;
    
System.out.println("After swaping numbers:"+x +y);
    }
}